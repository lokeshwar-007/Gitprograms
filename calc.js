console.log("SIMPLE CALCULATOR")
let ch = "%"
let a = 12
let b = 3
switch(ch){
    case '+':
        console.log('Addition is = ',a+b)
        break
    case '-':
        console.log('Subtraction is = ',a-b)
        break
    case '*':
        console.log('Multiplication is = ',a*b)
        break
    case '/':
        console.log('Division is = ',a/b)
        break
    case '%':
        console.log('Modulus is = ',a%b)
        break
}
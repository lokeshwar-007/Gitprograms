console.log("TO CHECK THE FREEZING POINT IN CELCIUS")
function freeze(cel){
    if (cel == 0){
        console.log("It is the freezing point in celcius!")
    }else if(cel > 0){
        console.log("It is above the freezing point!")
    }else{
        console.log("It is below the freezing point!!")
    }
}
freeze(0)